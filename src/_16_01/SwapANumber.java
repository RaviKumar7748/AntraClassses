package _16_01;

public class SwapANumber {
	public static void main(String[] args) {
		int input1=10;
		int input2=20;
		System.out.println("Before Swaping "+input1 +" "+input2);
		input1=(input1+input2);
		input2=input1-input2;
		input1=input1-input2;
		System.out.println("After Swaping "+input1 +" "+input2);
		
		
		
	}

}
